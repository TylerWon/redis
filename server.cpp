/* Type of Entry */
enum EntryType {
    STR,
    SORTED_SET
};

/**
 * Entry in the kv store.
 * 
 * The value of the Entry is one of str or zset depending on the type.
 */
struct Entry {
    HNode node;
    std::string key;    
    EntryType type;
    std::string str;
    SortedSet zset;
};

/* Simplified version of Entry used for look-ups */
struct LookupEntry {
    HNode node;
    std::string key;
};

/* Client connection to the server */
struct Conn {
    int fd = -1;
    // application's intention, for the event loop
    bool want_read = false;
    bool want_write = false;
    bool want_close = false;
    // buffered input and output
    Buffer incoming = Buffer();  // data to be parsed by the application
    Buffer outgoing = Buffer();  // responses generated by the application
    // timers
    IdleTimer idle_timer; // if expiry time reached, connection has been idle for too long
};

HMap kv_store; // key-value store
std::vector<Conn *> fd_to_conn; // map of all client connections, indexed by fd
std::vector<struct pollfd> pollfds; // array of pollfds for poll()
Queue idle_timers; // idle timers for active connections, timer closest to expiring is at the front of the queue

/**
 * Gets the address info for the machine running this program which can be used in bind().
 * 
 * @return  Pointer to a struct addrinfo on success. Should be freed when no longer in use.
 *          NULL on error.
 */
struct addrinfo *get_my_addr_info() {
    struct addrinfo *res;
    struct addrinfo hints;

    memset(&hints, 0, sizeof(hints));
    hints.ai_family = AF_UNSPEC;        // IPv4 or IPv6
    hints.ai_socktype = SOCK_STREAM;    // Stream socket
    hints.ai_flags = AI_PASSIVE;        // Returns wildcard address

    if (getaddrinfo(NULL, PORT, &hints, &res) != 0) {
        return NULL;
    }

    return res;
}

/**
 * Starts the server by creating a listener socket bound to a pre-defined port. 
 * 
 * @param res   Pointer to a struct addrinfo containing the addrinfo for the server.
 * 
 * @return  The listener socket on success.
 *          -1 on error.
 */
int start_server(struct addrinfo *res) {
    struct addrinfo *p;
    int listener;
    for (p = res; p != NULL; p = p->ai_next) {
        if ((listener = socket(p->ai_family, p->ai_socktype, p->ai_protocol)) == -1) {
            perror("socket()");
            continue;
        }

        int yes = 1;
        setsockopt(listener, SOL_SOCKET, SO_REUSEADDR, &yes, sizeof(yes));  // Allows port to be re-used

        if (bind(listener, p->ai_addr, p->ai_addrlen) == -1) {
            perror("bind()");
            close(listener);
            continue;
        }

        if (listen(listener, SOMAXCONN) == -1) {
            perror("listen()");
            close(listener);
            continue;
        }

        return listener;
    }

    return -1;
}

/**
 * Callback which checks if two hash map Entries are equal.
 * 
 * @param node1 The HNode contained by the first entry.
 * @param node2 The HNode contained by the second entry.
 * 
 * @return  True if the Entries are equal.
 *          False if not. 
 */
bool are_entries_equal(HNode *node1, HNode *node2) {
    Entry *entry1 = container_of(node1, Entry, node);
    Entry *entry2 = container_of(node2, Entry, node);
    return entry1->key == entry2->key;
}

/**
 * Searches for the Entry with the given key in the kv store.
 * 
 * @param key   The key of the entry to look for.
 * 
 * @return  Pointer to the Entry if found.
 *          NULL otherwise.
 */
Entry *lookup_entry(const std::string &key) {
    LookupEntry lookup_entry;
    lookup_entry.key = key;
    lookup_entry.node.hval = str_hash(key);
    HNode *node = kv_store.lookup(&lookup_entry.node, are_entries_equal);
    return node != NULL ? container_of(node, Entry, node) : NULL;
}

/**
 * Gets the entry for the provided key in the kv store.
 * 
 * If the key does not exist the special value nil is returned. 
 * An error is returned if the value stored at key is not a string.
 * 
 * @param key   The key to obtain.
 * 
 * @return  One of the following:
 *          - StrResponse: the value of the key.
 *          - NilResponse: if the key does not exist.
 *          - ErrResponse: if the value stored at the key is not a string.
 */
Response *do_get(const std::string &key) {
    Entry *entry = lookup_entry(key);

    if (entry == NULL) {
        return new NilResponse();
    } else if (entry->type != EntryType::STR) {
        return new ErrResponse(ErrResponse::ErrorCode::ERR_BAD_TYPE, "value is not a string");
    }

    return new StrResponse(entry->str);
}

/**
 * Sets the value of the provided key in the kv store. 
 * 
 * If key already exists, updates its value.
 * 
 * @param key   The key to set.
 * @param value The value for the key.
 * 
 * @return  StrResponse ("OK"): the key was set.
 */
Response *do_set(const std::string &key, const std::string &value) {
    Entry *entry = lookup_entry(key);

    if (entry != NULL) {
        entry->str = value;
    } else {
        entry = new Entry();
        entry->key = key;
        entry->type = EntryType::STR;
        entry->str = value;
        entry->node.hval = str_hash(key);
        kv_store.insert(&entry->node);
    }

    return new StrResponse("OK");
}

/**
 * Deletes the entry for the provided key in the kv store.
 * 
 * @param key   The key to delete.
 * 
 * @return  IntResponse: the number of keys removed.
 */
Response *do_del(const std::string &key) {
    LookupEntry lookup_entry;
    lookup_entry.key = key;
    lookup_entry.node.hval = str_hash(key);
    HNode *node = kv_store.remove(&lookup_entry.node, are_entries_equal);
    
    if (node != NULL) {
        delete container_of(node, Entry, node);
        return new IntResponse(1);
    }

    return new IntResponse(0);
}

/**
 * Callback which gets the key for an Entry in the hash map and stores it in the provided vector.
 * 
 * @param node  The HNode contained by the Entry which we want to get the key for.
 * @param arg   Void pointer to a vector to store the key in. Void pointer type allows other callbacks to have a 
 *              different type for their argument.
 */
void get_key(HNode *node, void *arg) {
    std::vector<std::string> &keys = *(std::vector<std::string> *) arg;
    Entry *entry = container_of(node, Entry, node);
    keys.push_back(entry->key);
}

/**
 * Gets all keys in the kv store.
 * 
 * @return ArrResponse: a list of keys.
 */
Response *do_keys() {
    std::vector<std::string> keys;
    kv_store.for_each(get_key, (void *) &keys);

    std::vector<Response *> elements;
    for (const std::string &key : keys) {
        elements.push_back(new StrResponse(key));
    }

    return new ArrResponse(elements);
}

/**
 * Adds a pair to the sorted set stored at the given key.
 *
 * If a pair with the given name already exists in the sorted set, its score is updated.
 * If key does not exist, a new sorted set with the specified pair is created.
 * If the key exists but does not hold a sorted set, an error is returned.
 *
 * @param key   The key of the sorted set.
 * @param score The pair's score.
 * @param name  The pair's name.
 *
 * @return  One of the following:
 *          - IntResponse: the number of new or updated pairs.
 *          - ErrResponse: the key does not hold a sorted set.
 */
Response *do_zadd(const std::string &key, double score, const std::string &name) {
    Entry *entry = lookup_entry(key);

    if (entry == NULL) {
        entry = new Entry(); // sorted set initialized when Entry created
        entry->key = key;
        entry->type = EntryType::SORTED_SET;
        entry->node.hval = str_hash(key);
        kv_store.insert(&entry->node);
    } else if (entry != NULL && entry->type != EntryType::SORTED_SET) {
        return new ErrResponse(ErrResponse::ErrorCode::ERR_BAD_TYPE, "value is not a sorted set");
    }

    entry->zset.insert(score, name.data(), name.length());

    return new IntResponse(1);
}

/**
 * Gets the score of name in the sorted set stored at key.
 * 
 * @param key   The key of the sorted set.
 * @param name  The name of the score to get.
 * 
 * @return  One of the following:
 *          - StrResponse: the score of the name.
 *          - NilResponse: if name does not exist in the sorted set, or the key does not exist.
 */
Response *do_zscore(const std::string &key, const std::string &name) {
    Entry *entry = lookup_entry(key);

    if (entry != NULL && entry->type == EntryType::SORTED_SET) {
        SPair *pair = entry->zset.lookup(name.data(), name.length());
        if (pair != NULL) {
            return new StrResponse(std::to_string(pair->score));
        }
    }

    return new NilResponse();
}

/**
 * Removes name from the sorted set stored at key.
 * 
 * If the key exists but does not hold a sorted set, an error is returned.
 * 
 * @param key   The key of the sorted set.
 * @param name  The name of the pair to remove.
 * 
 * @return  One of the following:
 *          - IntResponse: the number of pairs removed from the sorted set.
 *          - ErrResponse: the key does not hold a sorted set.
 */
Response *do_zrem(const std::string &key, const std::string &name) {
    Entry *entry = lookup_entry(key);

    if (entry == NULL) {
        return new IntResponse(0);
    } else if (entry != NULL && entry->type != EntryType::SORTED_SET) {
        return new ErrResponse(ErrResponse::ErrorCode::ERR_BAD_TYPE, "value is not a sorted set");
    }

    bool success = entry->zset.remove(name.data(), name.length());

    return success ? new IntResponse(1) : new IntResponse(0);
}

/**
 * Finds all pairs in the sorted set stored at key greater than or equal to the given pair.
 * 
 * If the key exists but does not hold a sorted set, an error is returned.
 * 
 * @param key       The key of the sorted set.
 * @param score     The pair's score.
 * @param name      The pair's name.
 * @param offset    The number of pairs to exclude from the beginning of the result.
 * @param limit     The maximum number of pairs to return.
 * 
 * @return  One of the following:
 *          - ArrResponse: the pairs greater than or equal to the given pair.
 *          - ErrResponse: the key does not hold a sorted set.
 */
Response *do_zquery(const std::string &key, double score, const std::string &name, uint64_t offset, uint64_t limit) {
    Entry *entry = lookup_entry(key);

    if (entry == NULL) {
        return new ArrResponse(std::vector<Response *>());
    } else if (entry != NULL && entry->type != EntryType::SORTED_SET) {
        return new ErrResponse(ErrResponse::ErrorCode::ERR_BAD_TYPE, "value is not a sorted set");
    }

    std::vector<SPair *> pairs = entry->zset.find_all_ge(score, name.data(), name.length(), offset, limit);
    std::vector<Response *> elements;
    for (const SPair *pair : pairs) {
        Response *score = new DblResponse(pair->score);
        Response *name = new StrResponse(std::string(pair->name, pair->len));
        elements.push_back(score);
        elements.push_back(name);
    }

    return new ArrResponse(elements);
}

/**
 * Gets the rank (position in sorted order) of name in the sorted set stored at key.
 * 
 * The rank is 0-based, so the lowest pair is rank 0.
 * 
 * @param key   The key of the sorted set.
 * @param name  The name of the pair to rank.
 * 
 * @return  One of the following:
 *          - NilResponse: if the key does not exist or the pair does not exist in the sorted set.
 *          - IntResponse: the rank of the pair.
 */
Response *do_zrank(const std::string &key, const std::string &name) {
    Entry *entry = lookup_entry(key);
    
    if (entry == NULL || entry->type != EntryType::SORTED_SET) {
        return new NilResponse();
    }

    int64_t rank = entry->zset.rank(name.data(), name.length());
    if (rank < 0) {
        return new NilResponse();
    }

    return new IntResponse(rank);
}

/**
 * Executes the command in the Request.
 * 
 * The following commands are supported:
 * 1. get key
 * 2. set key value
 * 3. del key
 * 4. keys
 * 5. zadd key score name
 * 6. zscore key name
 * 7. zrem key name
 * 8. zquery key score name offset limit
 * 9. zrank key name
 * 
 * Note: square brackets indicate optional arguments
 * 
 * @param request   Pointer to the Request.
 * 
 * @return  Pointer to the Response for executing the command.
 */
Response *execute_command(Request *request) {
    Response *response;
    std::vector<std::string> command = request->get_cmd();

    if (command.size() == 2 && command[0] == "get") {
        response = do_get(command[1]);
    } else if (command.size() == 3 && command[0] == "set") {
        response = do_set(command[1], command[2]);
    } else if (command.size() == 2 && command[0] == "del") {
        response = do_del(command[1]);
    } else if (command.size() == 1 && command[0] == "keys") {
        response = do_keys();
    } else if (command.size() == 4 && command[0] == "zadd") {
        response = do_zadd(command[1], std::stod(command[2]), command[3]);
    } else if (command.size() == 3 && command[0] == "zscore") {
        response = do_zscore(command[1], command[2]);
    } else if (command.size() == 3 && command[0] == "zrem") {
        response = do_zrem(command[1], command[2]);
    } else if (command.size() == 6 && command[0] == "zquery") {
        response = do_zquery(command[1], std::stod(command[2]), command[3], std::stol(command[4]), std::stoul(command[5]));
    } else if (command.size() == 3 && command[0] == "zrank") {
        response = do_zrank(command[1], command[2]);
    } else {
        response = new ErrResponse(ErrResponse::ErrorCode::ERR_UNKNOWN, "unknown command");
    }

    log(response->to_string().data());

    return response;
}

/**
 * Initializes the pollfds array from the map of open connections (fd_to_conn).
 */
void init_pollfds(int listener) {
    // reset from last event loop
    pollfds.clear();
    
    struct pollfd pfd = {listener, POLLIN, 0};
    pollfds.push_back(pfd);

    for (Conn *conn : fd_to_conn) {
        // conn set to NULL when connection is terminated
        if (conn == NULL) {
            continue;
        }

        pfd = {conn->fd, 0, 0};
        if (conn->want_read) {
            pfd.events |= POLLIN;
        }

        if (conn->want_write) {
            pfd.events |= POLLOUT;
        }

        pollfds.push_back(pfd);
    }
}

/**
 * Gets the time until the next timer expires.
 * 
 * @return  The time until the next timer expires.
 *          0 if the next timer has already expired.
 *          -1 if there are no active timers.
 */
int32_t get_time_until_timeout() {
    if (idle_timers.is_empty()) {
        return -1;
    }

    time_t now_ms = get_time_ms();
    QNode *node = idle_timers.front();
    IdleTimer *timer = container_of(node, IdleTimer, node);
    if (timer->expiry_time_ms <= now_ms) {
        return 0;
    }

    return (int32_t) (timer->expiry_time_ms - now_ms);
}

/** 
 * Sets a socket so that it is non-blocking.
 * 
 * @param fd    The socket to update.
 * 
 * @return  True on success.
 *          False on error.
 */
bool set_non_blocking(int fd) {
    // get current socket flags
    int flags = fcntl(fd, F_GETFL, 0);
    if (flags == -1) {
        return false;
    }

    // Add the O_NONBLOCK flag
    flags |= O_NONBLOCK;

    // update socket flags
    int result = fcntl(fd, F_SETFL, flags);
    if (result == -1) {
        return false;
    }

    return true;
}

/**
 * Handles a new connection on the listener socket.
 * 
 * @param listener  The listener socket.
 */
void handle_new_connection(int listener) {
    int client = accept(listener, NULL, NULL);
    if (client == -1) {
        log("failed to accept new connection");
        return;
    }

    if (!set_non_blocking(client)) {
        log("failed to set socket to non-blocking");
        close(client);
        return;
    }

    Conn *conn = new Conn();
    conn->fd = client;
    conn->want_read = true;

    if (fd_to_conn.size() < (uint32_t) conn->fd) {
        fd_to_conn.resize(conn->fd + 1);
    }

    fd_to_conn[conn->fd] = conn;
    idle_timers.push(&conn->idle_timer.node);
}

/**
 * Resets the expiry time of an idle timer.
 * 
 * Since its expiry time is reset, the timer is also moved to the end of the idle timer queue as it will now expire the 
 * latest.
 * 
 * @param timer The timer to reset.
 */
void reset_idle_timer(IdleTimer *timer) {
    timer->reset();
    idle_timers.remove(&timer->node);
    idle_timers.push(&timer->node);
}

/**
 * Receives data from the connection and stores it in the connection's input buffer.
 * 
 * @param conn  The connection to receive data from.
 * 
 * @return  True on success.
 *          False on error.
 */
bool recv_data(Conn *conn) {
    char buf[64 * 1024]; // 64 KB, large size is to handle pipelined requests
    int recvd = recv(conn->fd, buf, sizeof(buf), 0);
    if (recvd == -1 && errno == EAGAIN) {
        log("connection %d not actually ready to receive", conn->fd);
        return false;
    } else if (recvd < 0) {
        log("unexpected error when receiving data for connection %d", conn->fd);
        return false;
    } else if (recvd == 0) {
        if (conn->incoming.size() == 0) {
            log("peer terminated connection %d", conn->fd);
        } else {
            log("peer terminated connection %d unexpectedly", conn->fd);
        }
        conn->want_close = true;
        return false;
    }

    conn->incoming.append(buf, (uint32_t) recvd);

    return true;
}

/**
 * Tries to parse a request from the connectionâ€™s input buffer. Removes the request from the buffer if it is able to 
 * parse one.
 * 
 * @param conn  The connection to parse the request from.
 * 
 * @return  Pointer to the Request on success.
 *          NULL if request cannot be parsed.
 */
Request *parse_request(Conn *conn) {
    auto [request, status] = Request::unmarshal(conn->incoming.data(), conn->incoming.size());

    if (status == Request::UnmarshalStatus::INCOMPLETE_REQ) {
        return NULL;
    } else if (status == Request::UnmarshalStatus::REQ_TOO_BIG) {
        log("request in connection %d's buffer exceeds the size limit", conn->fd);
        conn->want_close = true;
        return NULL;
    }

    conn->incoming.consume(Request::HEADER_SIZE + (*request)->length());

    return (*request);
}

/**
 * Handles when data is ready to be sent on the connection.
 * 
 * @param conn  The connection to send data over.
 */
void handle_send(Conn *conn) {
    int sent = send(conn->fd, conn->outgoing.data(), conn->outgoing.size(), 0);
    if (sent == -1 && errno == EAGAIN) {
        log("connection %d not actually ready to send", conn->fd);
        return;
    } else if (sent < 0) {
        log("unexpected error when sending on connection %d", conn->fd);
        conn->want_close = true;
        return;
    } 

    conn->outgoing.consume((uint32_t) sent);

    if (conn->outgoing.size() == 0) {
        // nothing left to send for connection, change state from write to read 
        conn->want_read = true;
        conn->want_write = false;
    }
}

/**
 * Handles when data is ready to be received on the connection.
 * 
 * @param conn  The connection with data ready to be received.
 */
void handle_recv(Conn *conn) {
    if (!recv_data(conn)) {
        log("couldn't receive data on connection %d", conn->fd);
        return;
    }

    while (Request *request = parse_request(conn)) {
        log("connection: %d, request: %s", conn->fd, request->to_string().data());

        Response *response = execute_command(request);
        if (response->marshal(conn->outgoing) == Response::MarshalStatus::RES_TOO_BIG) {
            log("response to connection %d exceeds the size limit", conn->fd);

            delete response;
            response = new ErrResponse(ErrResponse::ErrorCode::ERR_TOO_BIG, "response is too big");
            response->marshal(conn->outgoing);

            conn->want_close = true;
            delete response;

            return;
        }

        delete request;
        delete response;
    }

    if (conn->outgoing.size() > 0) {
        // something to send for connection, change state from read to write
        conn->want_read = false;
        conn->want_write = true;
        handle_send(conn); // The socket is likely ready to write in a request-response protocol, try to write it 
                           // without waiting for the next iteration
    }
}

/**
 * Handles when the connection should be closed.
 * 
 * @param conn  The connection to close.
 */
void handle_close(Conn *conn) {
    int fd = conn->fd;

    close(conn->fd);
    fd_to_conn[conn->fd] = NULL;
    idle_timers.remove(&conn->idle_timer.node);
    delete conn;

    log("closed connection %d", fd);
}

/**
 * Checks the queue of idle timers to see if any have expired.
 * 
 * If a timer has expired, the associated connection is removed.
 */
void process_idle_timers() {
    time_t now_ms = get_time_ms();
    while (!idle_timers.is_empty()) {
        QNode *node = idle_timers.front();
        IdleTimer *timer = container_of(node, IdleTimer, node);
        if (timer->expiry_time_ms > now_ms) {
            break;
        }
        Conn *conn = container_of(timer, Conn, idle_timer);
        log("connection %d exceeded idle timeout", conn->fd);
        handle_close(conn);
    }
}

int main() {
    struct addrinfo *res = get_my_addr_info();
    if (res == NULL) {
        fatal("failed to get server's addrinfo");
    }

    int listener;
    if ((listener = start_server(res)) == -1) {
        fatal("failed to start server");
    }

    log("started server");

    while (true) {
        init_pollfds(listener);

        if (poll(pollfds.data(), pollfds.size(), get_time_until_timeout()) == -1) {
            fatal("failed to poll");
        }

        // listener socket always at index 0 of pollfds
        if (pollfds[0].revents & POLLIN) {
            handle_new_connection(listener);
        }

        for (uint32_t i = 1; i < pollfds.size(); i++) {
            uint16_t revents = pollfds[i].revents;
            if (revents == 0) {
                continue;
            }
    
            Conn *conn = fd_to_conn[pollfds[i].fd];
            reset_idle_timer(&conn->idle_timer);

            if (revents & POLLIN) {
                handle_recv(conn);
            }

            if (revents & POLLOUT) {
                handle_send(conn);
            }

            if (revents & POLLERR || conn->want_close) {
                handle_close(conn);
            }
        }

        process_idle_timers();
    }
}
